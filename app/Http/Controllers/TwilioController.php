    public function sendMessage($message, $recipients)
    {
        $GetAdminTwilioDetails = TwilioSetting::where('user_id', 1)->first();
        $account_sid = $GetAdminTwilioDetails->twilio_sid;
        $auth_token = $GetAdminTwilioDetails->twilio_token;
        $twilio_number = $GetAdminTwilioDetails->from_number;
        $client = new Client($account_sid, $auth_token);
        $client->messages->create($recipients, ['from' => $twilio_number, 'body' => $message]);
    }
